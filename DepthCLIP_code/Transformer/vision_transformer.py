def window_partition(x,windows_H,windows_W):
    B, H, W, C = x.shape
    x = x.view(B, H // windows_H, windows_H, W // windows_W, windows_W, C)
    windows = x.permute(2, 4, 0, 1, 3, 5).contiguous().view(-1, B, x.shape[1], x.shape[3], C)
    return windows


def window_reverse(windows, H, W):
    B = windows.shape[1]
    x = windows.view(H // windows.shape[2], W // windows.shape[3], B, windows.shape[2], windows.shape[3], -1)
    x = x.permute(2, 3, 0, 4, 1, 5).contiguous().view(B, H, W, -1)
    return x



class Mlp(nn.Module):
    def __init__(self, in_features, hidden_features=None, out_features=None, act_layer=nn.GELU, drop=0.):
        super().__init__()
        out_features = out_features or in_features
        hidden_features = hidden_features or in_features
        self.fc1 = nn.Linear(in_features, hidden_features)
        self.act = act_layer()
        self.fc2 = nn.Linear(hidden_features, out_features)
        self.drop = nn.Dropout(drop)

    def forward(self, x):
        x = self.fc1(x)
        x = self.act(x)
        x = self.drop(x)
        x = self.fc2(x)
        x = self.drop(x)
        return x

